numero_primario =  input("Digite um valor numerico: ")
numero_secundario = input("Digite outro valor numerico: ")
 

if numero_primario > numero_secundario:
    print(f" O primeiro valor: {numero_primario} é maior que o segundo valor: {numero_secundario}")
  
elif numero_primario < numero_secundario:
    print(f"O segundo valor: {numero_secundario} é maior que o primeiro valor: {numero_primario}")
    
else:
    print("Porfavor tente novamente!")
    
    
